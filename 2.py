# Из трех действительных чисел a, b и c выбрать те, модули которых не меньше 4.

#!/usr/bin/env python3
# -*- coding: utf-8 -*-

if __name__ == '__main__':

    a = 3
    b = -5
    c = 6

    if(abs(a) > 4):
        print(a)
    if(abs(b) > 4):
        print(b)
    if(abs(c) > 4):
        print(c)
